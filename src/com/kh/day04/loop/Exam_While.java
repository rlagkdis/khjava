package com.kh.day04.loop;

public class Exam_While {
	public static void main(String[] args) {
		int i = 0;
		while(i < 10) {
			System.out.print(i);
			i++;
		}
		System.out.println();  //줄바꾸기
		int j = 9;
		while(j >= 0) {
			System.out.print(j);
			j--;
		}
		//for(int j = 9; j >=0; j--) {
		// System.out.print(j); }
	}

}
